const ingredients = [
  'Potatoes',
  'Mushrooms',
  'Garlic',
  'Tomatos',
  'Herbs',
  'Condiments',
];
const ul = document.querySelector('#ingredients');

const items = ingredients.map((ingridient) => {
  let item = document.createElement("li");
  item.textContent = `${ingridient }`;
  item.classList.add("item");
  return item;
});
ul.append(...items);




